import pefile
import sys

def list_exported_symbols(pe_file_path):
    try:
        pe = pefile.PE(pe_file_path)
        if hasattr(pe, 'DIRECTORY_ENTRY_EXPORT'):
            for exp in pe.DIRECTORY_ENTRY_EXPORT.symbols:
                if exp.name is not None:
                    print(f"{exp.name.decode('utf-8')} at {hex(exp.address)}")
                else:
                    # Some symbols might not have a name (exported by ordinal only)
                    print(f"Ordinal {exp.ordinal} at {hex(exp.address)}")
        else:
            print("No exported symbols found.")
    except pefile.PEFormatError as e:
        print(f"Error: {e}")

if __name__ == "__main__":
    if len(sys.argv) != 2:
        print("Usage: python extract_exports.py <PE file>")
    else:
        pe_file_path = sys.argv[1]
        list_exported_symbols(pe_file_path)

